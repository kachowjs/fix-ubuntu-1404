app_run_apt_install () {

	if [ -f $THE_PACKAGE_LIST_FILE_PATH ]; then
		sudo apt-get install $(cat $THE_PACKAGE_LIST_FILE_PATH)
	fi

}

app_run_clean () {
	# rm tmp dir
	if [ -d $THE_TMP_DIR_PATH ]; then
		echo 'rm '$THE_TMP_DIR_PATH
		rm $THE_TMP_DIR_PATH -rf
	fi
}

app_run_download() {
	# change to dir tmp
	mkdir $THE_TMP_DIR_PATH -p
	cd $THE_TMP_DIR_PATH

	if [ -f $THE_DOWNLOAD_LIST_FILE_PATH ]; then
		wget -c -i $THE_DOWNLOAD_LIST_FILE_PATH
	fi
}
